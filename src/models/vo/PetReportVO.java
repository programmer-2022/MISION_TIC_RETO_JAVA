package models.vo;


public class PetReportVO {

    private int count;
    private String specie;
        
    public PetReportVO() { }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public String getSpecie() {
        return specie;
    }

    public void setSpecie(String specie) {
        this.specie = specie;
    }   
}